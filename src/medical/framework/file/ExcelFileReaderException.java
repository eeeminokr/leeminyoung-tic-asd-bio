package medical.framework.file;

/**
 * @author jaechulhan
 * 
 */
public class ExcelFileReaderException extends Exception {

	private static final long serialVersionUID = -5239322760519022672L;

	public ExcelFileReaderException() {
		super();
	}

	public ExcelFileReaderException(String message) {
		super(message);
	}

	public ExcelFileReaderException(Throwable cause) {
		super(cause);
	}

	public ExcelFileReaderException(String message, Throwable cause) {
		super(message, cause);
	}

}
