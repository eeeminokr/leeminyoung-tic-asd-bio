package medical.framework.file.mapping;

public class CellReaderMapperException extends Exception {

	private static final long serialVersionUID = -9217092742505726624L;

	public CellReaderMapperException() {
		super();
	}

	public CellReaderMapperException(String message) {
		super(message);
	}

	public CellReaderMapperException(Throwable cause) {
		super(cause);
	}

	public CellReaderMapperException(String message, Throwable cause) {
		super(message, cause);
	}

}
